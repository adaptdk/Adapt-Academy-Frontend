/**
 * JavaScript is a loosely typed or a dynamic language.
 * Variables in JavaScript are not directly associated
 * with any particular value type, and any variable can
 * be assigned (and re-assigned) values of all types.
 * 
 * Primitive types are copied by their value, reference
 * types are copied by their address in memory.
 */

// Primitive types:
const boolValue = true // boolean.
const num = 25 // Number.
const text = 'Irmantas' // String.
const nonExistant = null // null.
const notDefined = undefined // undefined.

// Reference types:
const obj1 = { name: 'Irmantas', language: 'Javascript'} // Object type.